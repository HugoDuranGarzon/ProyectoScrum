    //Metodo para recoger todos los registros existentes y listarlos en la tabla
    //Se llama a este metodo con el boton Actualizar y en el constructor
    public void listar(){
        
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("DNI");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellidos");
            modelo.addColumn("Sueldo");
            modelo.addColumn("Fecha");
            modelo.addColumn("Matricula");
            
            
            
            DaoTrabajador t = new DaoTrabajador();
            
            ArrayList<Trabajador> listado=t.get();
            
            for(Trabajador trabajador: listado){
                ArrayList fila = new ArrayList();
                fila.add (trabajador.getDni());
                fila.add (trabajador.getNombre());
                fila.add (trabajador.getApellidos());
                fila.add (trabajador.getSueldo());
                fila.add (trabajador.getFecha());
                fila.add (trabajador.getMatricula());
                
                modelo.addRow(fila.toArray());
            }
            
            tabla.setModel(modelo);
            int nRegistros = listado.size();
            String SueldoMedio = d.SueldoMedio();
            ntrab.setText(""+nRegistros);
            media.setText(SueldoMedio);
        } catch (SQLException ex) {
            Logger.getLogger(FrmTrabajadores.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void listar_filtrado(Trabajador t, String comparador_sueldo,String comparador_fecha){
        
        DefaultTableModel modelo_filtrado = new DefaultTableModel();
        modelo_filtrado.addColumn("DNI");
        modelo_filtrado.addColumn("Nombre");
        modelo_filtrado.addColumn("Apellidos");
        modelo_filtrado.addColumn("Sueldo");
        modelo_filtrado.addColumn("Fecha");
        modelo_filtrado.addColumn("Matricula");
        ArrayList<Trabajador> filtrado= d.filtrar(t, comparador_sueldo, comparador_fecha);
        for(Trabajador trabajador: filtrado){
            ArrayList fila_filtrado = new ArrayList();
            fila_filtrado.add (trabajador.getDni());
            fila_filtrado.add (trabajador.getNombre());
            fila_filtrado.add (trabajador.getApellidos());
            fila_filtrado.add (trabajador.getSueldo());
            fila_filtrado.add (trabajador.getFecha());
            fila_filtrado.add (trabajador.getMatricula());
            
            modelo_filtrado.addRow(fila_filtrado.toArray());
        }
        tabla.setModel(modelo_filtrado);
        
    }
